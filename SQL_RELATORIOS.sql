JOIN CIDADES
    ON CIDADES.CIDADE = FUNCIONARIOS.CIDADE
JOIN BAIRRO
    ON BAIRRO.CIDADE = FUNCIONARIOS.CIDADE
    AND BAIRRO.BAIRRO = FUNCIONARIOS.BAIRRO
JOIN LOGRADOURO
    ON LOGRADOURO.CIDADE = FUNCIONARIOS.CIDADE
    AND LOGRADOURO.LOGRADOURO = FUNCIONARIOS.LOGRADOURO
JOIN TPLOGRADOURO
    ON TPLOGRADOURO.TIPO = LOGRADOURO.TIPO
;   

