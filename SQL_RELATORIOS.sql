SELECT * FROM
(
    SELECT FUNCIONARIOS.MATRICULA,
        FUNCIONARIOS.NOME,
        CALCULOS.REFERENCIA,
        CALCULOS.VALOR,
        CALCULOS.CONTA
    FROM FUNCIONARIOS
    JOIN CALCULOS
        ON CALCULOS.EMPRESA = FUNCIONARIOS.EMPRESA
        AND CALCULOS.MATRICULA = FUNCIONARIOS.MATRICULA
    JOIN TIPOFOLHA
        ON TIPOFOLHA.TIPOFOLHA = CALCULOS.TIPOFOLHA
        AND CALCULOS.CONTA BETWEEN 9000 AND 9002
) PIVOT (
    SUM(VALOR)
    FOR CONTA
    IN( 9000 AS VANTAGENS,
        9001 AS DESCONTOS,
        9002 AS LIQUIDO )
)
ORDER BY MATRICULA
;

