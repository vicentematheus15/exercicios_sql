SELECT
    funcionarios.matricula,
    funcionarios.nome,
    funcionarios.cpf,
    funcionarios.admissao,
    cargos.descricao AS cargo,
    orgaos.descricao AS secretaria,
    funcionarios.dtnascimento
FROM
         funcionarios
    JOIN cargos 
        ON cargos.empresa = funcionarios.empresa
        AND cargos.cargo = funcionarios.cargo
    JOIN setores 
        ON setores.empresa = funcionarios.empresa
        AND setores.setor = funcionarios.setor
    JOIN divisoes 
        ON divisoes.empresa = setores.empresa
        AND divisoes.codigo = setores.divisao
    JOIN orgaos 
        ON orgaos.empresa = divisoes.empresa
        AND orgaos.codigo = divisoes.orgao;
