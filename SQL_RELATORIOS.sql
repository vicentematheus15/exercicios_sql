SELECT
    FUNCIONARIOS.MATRICULA,
    FUNCIONARIOS.NOME,
    PERIODOS.REFERENCIA AS DATA_REFERENCIA,
    NVL(TO_CHAR(PERIODOS.DTPGTO, 'dd/mm/yyyy'), 'NÃO INFORMADO' ) AS DATA_PAGAMENTO,
    NVL(TO_CHAR(FUNCIONARIOS.HRMENSAIS), 'NÃO INFORMADO' )AS CARGA_HORARIA,
    SETORES.DESCRICAO AS LOTACAO,
    BANCOS.DESCRICAO AS BANCO    
FROM FUNCIONARIOS
JOIN SETORES
    ON SETORES.EMPRESA = FUNCIONARIOS.EMPRESA
    AND SETORES.SETOR = FUNCIONARIOS.SETOR
JOIN BANCOS
    ON BANCOS.NRBANCO = FUNCIONARIOS.BCOPGTO
JOIN CALCULOS
        ON CALCULOS.EMPRESA = FUNCIONARIOS.EMPRESA
        AND CALCULOS.MATRICULA = FUNCIONARIOS.MATRICULA
JOIN TIPOFOLHA
        ON TIPOFOLHA.TIPOFOLHA = CALCULOS.TIPOFOLHA
JOIN PERIODOS
    ON PERIODOS.TIPOFOLHA = TIPOFOLHA.TIPOFOLHA
WHERE FUNCIONARIOS.MATRICULA = 1
GROUP BY (FUNCIONARIOS.MATRICULA, FUNCIONARIOS.NOME, PERIODOS.DTPGTO, PERIODOS.REFERENCIA, FUNCIONARIOS.HRMENSAIS, SETORES.DESCRICAO, BANCOS.DESCRICAO)
ORDER BY PERIODOS.REFERENCIA
;

SELECT
    FUNCIONARIOS.MATRICULA,
    FUNCIONARIOS.NOME,
    PERIODOS.REFERENCIA AS DATA_REFERENCIA,
    PERIODOS.DTPGTO AS DATA_PAGAMENTO,
    NVL(TO_CHAR(FUNCIONARIOS.HRMENSAIS), 'NÃO INFORMADO' )AS CARGA_HORARIA,
    SETORES.DESCRICAO AS LOTACAO,
    BANCOS.DESCRICAO AS BANCO    
FROM FUNCIONARIOS
JOIN SETORES
    ON SETORES.EMPRESA = FUNCIONARIOS.EMPRESA
    AND SETORES.SETOR = FUNCIONARIOS.SETOR
JOIN BANCOS
    ON BANCOS.NRBANCO = FUNCIONARIOS.BCOPGTO
JOIN EMPRESAS
    ON EMPRESAS.EMPRESA = FUNCIONARIOS.EMPRESA
JOIN PERIODOS
    ON PERIODOS.EMPRESA = EMPRESAS.EMPRESA
WHERE FUNCIONARIOS.MATRICULA = 1
GROUP BY (FUNCIONARIOS.MATRICULA, FUNCIONARIOS.NOME, PERIODOS.DTPGTO, PERIODOS.REFERENCIA, FUNCIONARIOS.HRMENSAIS, SETORES.DESCRICAO, BANCOS.DESCRICAO)
ORDER BY PERIODOS.REFERENCIA
;

